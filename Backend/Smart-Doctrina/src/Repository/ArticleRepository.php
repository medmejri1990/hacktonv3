    {
        parent::__construct($registry, Article::class);
    }

    
    public function findByTagsField($value)
    {
        return $this->createQueryBuilder('a')
            ->join('a.tags', 't')
            ->andWhere($qb->expr()->eq('t.id', 'a.id'))
            ->andWhere('t.title = :val')
            ->setParameter('val', $value)
            ->orderBy('a.id', 'ASC')
            ->setMaxResults(10)
            ->getQuery()
            ->getResult()
        ;
    }

